import numpy as np

# Create a 1-dimensional array
a = np.array([1, 2, 3, 4, 5, 6])
print(a)


# Create a 2-dimensional array
b = np.array([[1, 2, 3], [4, 5, 6]])
print(b)

# Array math
print(b + 2)
print(b * 10)
print(b + b)

